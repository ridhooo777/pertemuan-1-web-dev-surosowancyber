// Operator Aritmatika
let a = 10;
let b = 5;

console.log(a + b); // 15
console.log(a - b); // 5
console.log(a * b); // 50
console.log(a / b); // 2
console.log(a % b); // 0
console.log(a ** b); // 100000



// Operator Perbandingan
let nilai = 80;

console.log(nilai > 70); // true
console.log(nilai < 70); // false
console.log(nilai == "80"); // true
console.log(nilai !== 80); // true

// Operator Logika
let usernameBenar = true;
let passwordBenar = true;

console.log(usernameBenar && passwordBenar); // true
console.log(usernameBenar || passwordBenar); // true
console.log(!usernameBenar);